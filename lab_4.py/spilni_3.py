n = int(input("Введіть поточну кількість скринь у Чахлика: "))
m = int(input("Введіть кількість скринь, що він додає щороку: "))
k = int(input("Введіть кількість скринь, яку потрібно мати для виходу заміж: "))

years = 0

while n < k:
    n += m
    years += 1

print(f"Час, коли Василина зможе вийти заміж: {years} років")
