a,b = map(float, input("Введіть розміри острова a на b ").split())
s = float(input("Введіть площу на одного робінзона "))
k = float(input("Введіть кількість робінзонів на острові "))
Square = a*b
if Square>=k*s:
    print("Норми не порушуються")
    t = (Square-k*s)//s
    if t>0:
        print("Цей острів може прийняти ще {:n} робінзонів".format(t))
    else:
        print("Прийняти на острів більше не має можливості")
else:
    print("Норми проживання порушуються!")